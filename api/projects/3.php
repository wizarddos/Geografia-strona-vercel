    <section class="excerpt text-middle">
        <h2>Zdefiniowanie wyzwania</h2>
        Jesteśmy 6 osobową grupą uczniów z drugiej klasy warszawskiego liceum im. Klementyny Hoffmanowej. W ramach projektu związanego z metodą Design Thinking będziemy przygotowywać akcję, która ma na celu choć odrobinę poprawić humor osobom poruszającym się po zatłoczonym centrum Warszawy. 
    </section>

    <section class="text-middle">
        <h2>Określenie celów</h2>
        Chcemy żebyście choć na chwilę zatrzymali się i uśmiechnęli. W jaki sposób będziemy to realizować dowiecie się w dalszej części projektu, ale już teraz możemy wam powiedzieć, że efektów naszej pracy prawdopodobnie będzie można wypatrywać w okolicach złotych tarasów, metra centrum oraz przy samej szkole, do której uczęszczamy. 
    </section>

