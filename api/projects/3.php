    <section class="text-middle">
        <h2>Zdefiniowanie ról</h2>
        W najbliższym czasie, podzielimy nasz zespół na grupy, którym przydzielane będą odpowiednie zadania, mające na celu zbliżyć nas do ukończenia projektu. Jedna z grup będzie odpowiedzialna za dostarczenie informacji w jaki sposób projekt powinien zostać wprowadzony w życie, w czasie kiedy druga już będzie przygotowywała go do faktycznego wdrożenia. Myślimy także nad wyznaczeniem jednej osoby, któta będzie odpowiedzialna za kontrolowanie naszej grupy oraz dokładne opisywanie wam naszych postępów. 
    </section>

