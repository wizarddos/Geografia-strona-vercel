    <section class="text-middle">
        <h2>Przygotowanie planu projektu</h2>
        Nasz zespół wie dokładnie co ma robić i w jakim czasie ma to wykonać. Staramy się  wcześniej wszystko rozplanować, tak aby później nie martwić się o uciekający czas. Oczywiście nie wszystko da się przewidzieć, jednak to, co wiemy staramy się mądrze wykorzystać.
        <ol>
            <li>Przekazaliśmy Szkole Podstawowej nr. 25 ankietę, z której dowiedzieliśmy się, że spośród trzech zaproponowanych przez nas warsztatów obejmujących historię, polski lub fizykę, młodsi najbardziej zainteresowani byliby pierwszą opcją.</li>
            <li> Po rozmowie z dyrektorką tejże placówki uzyskaliśmy zgodę na przeprowadzenie warsztatów z dziećmi.</li>
            <li>Przez kilka następnych tygodni będziemy planować, co dokładnie chcemy przekazać uczniom i w jaki sposób chcemy to wykonać.</li>
            <li>Po wykonaniu schematu warsztatów planujemy wcielić go w życie, co będzie miało miejsce prawdopodobnie na przełomie marca i kwietnia.</li>
        </ol>
