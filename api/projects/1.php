        <h2>Zdefiniowanie wyzwania</h2>
        Naszym głównym wyzwaniem jest dotarcie do jak największej liczby uczniów prezentując im bardzo istotny i wszechobecny problem zanieczyszczenia powietrza w Warszawie. Potencjalne przeszkody, które mogą być obecne w naszej akcji, to znalezienie sponsora projektu oraz nagłośnienie akcji. Nasz zespół projektowy postara się jednak w sposób elastyczny odpowiadać na wyniki działań i dopasowywać się do nich.
