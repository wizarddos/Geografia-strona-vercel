        <h2>Zdefiniowanie ról</h2>
        Za sprawiedliwy podział ról w naszym zespole i delegowanie zadań odpowiedzialny jest lider grupy, biorąc pod uwagę indywidualne preferencje poszczególnych członków. Szczegółowe role nie są jeszcze do końca zdefiniowane, natomiast wraz z postępem projektu zostaną one wyklarowane.
    </section>
    <section class="text-middle">
        <h2>Przygotowanie narzędzi</h2>
        Narzędzia, których spodziewany się używać w trakcie przeprowadzania projektu, to Messenger, Discord i Slack do ułatwienia komunikacji pomiędzy członkami zespołu, Google Forms do przeprowadzania ankiet, Visual Studio Code do potencjalnej części programistycznej.
    </section>
    
    <section class="text-middle">
        <h2>Przygotowanie planu projektu</h2>
        Niewątpliwie trzonem naszego projektu jest współpraca ze sponsorem. Pierwszym krokiem było otrzymanie zgody na nasz pomysł od Pani Dyrektor. Następnie postaramy się skontaktować ze sponsorem, czego wyniki stanowią podstawę do zdefiniowania dalszych etapów pracy.
    </section>

    <section class="text-middle">
        <h2>Postępy projektu</h2>
        Całym zespołem dopracowaliśmy już nasz pomysł na ostateczny wygląd projektu. Potwierdzone
        zostały aspekty takie jak cel, przebieg i organizacja. Pozostała nam jedynie kwestia sfinansowania
        projektu i podzielenie się z grupą docelową szczegółami akcji.
    </section>

    <section class="text-middle">
        <h2>Nad czym obecnie pracujemy?</h2>
        Jako, że nasz projekt jest silnie uzależniony od odpowiedzi potencjalnych sponsorów, poświęcamy
        dużo uwagi dopracowaniu szczegółów i złapaniu kontaktu. Obecnie jesteśmy w trakcie pisania maili
        do sponsorów.

        Pojawiła się w zespole propozycja, która wykluczyłaby możliwość przegapienia naszej prośby przez
        firmę, a mianowicie bardziej bezpośredni kontakt przez pośrednika. Obecnie jest on rozważany,
        jednak jeszcze nie ustaliliśmy, czy ostatecznie dojdzie do skutku.
    </section>
